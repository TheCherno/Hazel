outputdir = "%{cfg.buildcfg}-%{cfg.system}-%{cfg.architecture}"

function output_dir(name)
	return "%{wks.location}/build/" .. name .. "/" .. outputdir .. "/%{prj.name}"
end

-- Include directories relative to root folder (solution directory)
